    private void tableUpdateStudentInformationMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableUpdateStudentInformationMouseClicked
        // TODO add your handling code here:
        
        //Get the selected Row Information when clicked
        DefaultTableModel model = (DefaultTableModel) tableUpdateStudentInformation.getModel();
        int selectedRowIndex = tableUpdateStudentInformation.getSelectedRow();
        fNameUpdateStudentInformation.setText(model.getValueAt(selectedRowIndex, 1).toString());
        lNameUpdateStudentInformation.setText(model.getValueAt(selectedRowIndex, 2).toString());
        courseUpdateStudentInformation.setSelectedItem(model.getValueAt(selectedRowIndex, 4));
        String semSel = model.getValueAt(selectedRowIndex, 5).toString();
        semUpdateStudentInformation.setSelectedIndex(Integer.valueOf(semSel)-1);
        addressUpdateStudentInformation.setText(model.getValueAt(selectedRowIndex, 6).toString());
        emailUpdateStudentInformation.setText(model.getValueAt(selectedRowIndex, 7).toString());
        phoneUpdateStudentInformation.setText(model.getValueAt(selectedRowIndex, 8).toString());
        String maleFemale = (String)model.getValueAt(selectedRowIndex, 3);
        //Check the radio button
        if(maleFemale.equals(maleUpdateStudentInformation.getText())){
            maleUpdateStudentInformation.setSelected(true);
        }else{
            femaleUpdateStudentInformation.setSelected(true);
        }
        
        String selectedId = model.getValueAt(selectedRowIndex, 0).toString();
        getSelectedId = Integer.valueOf(selectedId);
        btnRegisterNUpdateStudentInformation.setEnabled(true);
        
        
    }//GEN-LAST:event_tableUpdateStudentInformationMouseClicked
    
    
    public boolean maleFemale(){
        ButtonGroup bg = new ButtonGroup();
        bg.add(maleUpdateStudentInformation);
        bg.add(femaleUpdateStudentInformation);
        if(bg.getSelection() == null){
            return true;
        }else{
            return false;
        }
    }
    
