    private void btnRegisterNUpdateStudentInformationActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterNUpdateStudentInformationActionPerformed
        // TODO add your handling code here:
        RegisterNewStudent rnw = new RegisterNewStudent();
        int n = JOptionPane.showConfirmDialog(null, "Are you sure you want to update?", "Select an option", JOptionPane.WARNING_MESSAGE);
        if (n == JOptionPane.YES_OPTION){
            String fName = fNameUpdateStudentInformation.getText();
            String lName = lNameUpdateStudentInformation.getText();
            String address = addressUpdateStudentInformation.getText();
            String email = emailUpdateStudentInformation.getText();
            String phone = phoneUpdateStudentInformation.getText();
            String course = (String)courseUpdateStudentInformation.getSelectedItem();
            String semester = (String)semUpdateStudentInformation.getSelectedItem();
            String gender;
            if(maleUpdateStudentInformation.isSelected()){
                gender = maleUpdateStudentInformation.getText();
            }else{
                gender = femaleUpdateStudentInformation.getText();
            }

            boolean maleFemaleCheck = maleFemale();
            if(rnw.checkIfValueCorrect(fName, lName, address, email, phone,maleFemaleCheck)){
                if(!SignUp.isValidEmailAddress(email)){
                    JOptionPane.showMessageDialog(null, "Enter Valid email address", "Try Again", JOptionPane.ERROR_MESSAGE);
                }else if(SignUp.validPhone(phone)){
                    JOptionPane.showMessageDialog(null, "Enter Valid Phone Number", "Try Again", JOptionPane.ERROR_MESSAGE);
                }else{
                    InsertIntoDatabase insDatabase = new InsertIntoDatabase();
                    insDatabase.updateIntoDatabase(getSelectedId,fName,lName,gender,course,semester,address,email,phone);
                    JOptionPane.showMessageDialog(null, "Data Updated Sucessfully", "Sucess", JOptionPane.INFORMATION_MESSAGE);
                    this.dispose();
                    new UpdateStudentInformation().setVisible(true);
                }
            } 
        }
        
    }//GEN-LAST:event_btnRegisterNUpdateStudentInformationActionPerformed

